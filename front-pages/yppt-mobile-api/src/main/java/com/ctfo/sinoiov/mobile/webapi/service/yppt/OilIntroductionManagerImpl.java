@Service
public class OilIntroductionManagerImpl implements IJsonService {

	protected static final Log logger = LogFactory.getLog(OilIntroductionManagerImpl.class);
	
	@Override
	public Body execute(Parameter request, Object... obj)
			throws ClientException {
		logger.info(">>>>>>>>>>>>>>>>获取静态页面URL<<<<<<<<<<<<<<<<<<<");
		OilIntroductionRsp response = new OilIntroductionRsp();
		OilIntroductionReq req = (OilIntroductionReq) request.getBody();
		if(StringUtils.isNotBlank(req.getKey())){
			response.setUrl(PropertyImageUtils.getUrlByKey(req.getKey().toUpperCase()));
		}else{
			response.setUrl(PropertyImageUtils.getUrlByKey("HTML_INDEX"));
		}
		logger.info(">>>>>>>>>>>>>>>>获取静态页面URL<<<<<<<<<<<<<<<<<<<");
		return response;
	}

	@Override
	public void validate(Parameter request) throws ClientException {
		

	}

	@Override
	public Parameter convertParameter(String request) {
		try{
			Map<String, Class<?>> classMap = new HashMap<String, Class<?>>();
	        classMap.put("head", Head.class);
	        classMap.put("body", OilIntroductionReq.class);
	        Parameter param = (Parameter)JSONObject.toBean(JSONObject.fromObject(request),Parameter.class , classMap);
	        return param;
		}catch(Exception e){
			throw new ClientException("E000005",Common.E000005);
		}
	}

}
